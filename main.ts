function 加入隨機遊蕩的行為 (sprite: Sprite) {
    sprite.setStayInScreen(true)
    spriteutils.onSpriteUpdateInterval(sprite, 500, function (sprite) {
        if (Math.percentChance(50)) {
            if (Math.percentChance(50)) {
                sprite.setVelocity(50, 0)
            } else {
                sprite.setVelocity(-50, 0)
            }
        } else {
            if (Math.percentChance(50)) {
                sprite.setVelocity(0, 50)
            } else {
                sprite.setVelocity(0, -50)
            }
        }
    })
}
function 加入何時關閉跟隨的行為 (sprite: Sprite, toFollow: Sprite) {
    spriteutils.onSpriteUpdateInterval(sprite, 500, function (sprite) {
        if (spriteutils.distanceBetween(sprite, toFollow) < 40) {
            sprite.follow(toFollow, 50)
        } else {
            sprite.follow(toFollow, 0)
        }
    })
}
